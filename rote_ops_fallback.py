def load_wiki_ops_names():
    raw = zlib.decompress(base64.b64decode(_WIKI_OPS_DATA_B64.encode("ascii"))).decode("utf-8")
    return json.loads(raw)


def build_wiki_tb_defs(resolve_name_to_defid, placeholder_defid, is_ship_name):
    raw_defs = load_wiki_ops_names()
    out = {}
    for planet_id, platoons in raw_defs.items():
        planet_relic = _ZONE_RELIC_BY_PLANET.get(planet_id, 0)
        out[planet_id] = []
        for platoon in platoons:
            slots = []
            for display_name in platoon:
                def_id = resolve_name_to_defid(display_name) or placeholder_defid(display_name)
                slots.append({
                    "defId": def_id,
                    "name": display_name,
                    "minRarity": 7,
                    "minRelic": 0 if is_ship_name(display_name, def_id) else planet_relic,
                })
            out[planet_id].append(slots)
    return out
